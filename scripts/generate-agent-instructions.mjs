import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { format, resolveConfig } from "prettier";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoDir = path.resolve(__dirname, "..");
const docsDir = path.join(repoDir, "website", "docs", "ai");
const packageJsonPath = path.join(repoDir, "package.json");
const packageJson = JSON.parse(readFileSync(packageJsonPath, "utf8"));
const siteBaseUrl = new URL(packageJson.homepage).toString().replace(/\/$/, "");
const prettierConfig = (await resolveConfig(packageJsonPath)) ?? {};
const checkMode = process.argv.includes("--check");

const generatedBlockStart = "<!-- REACT_AMNESIA_AGENT_PACK:START -->";
const generatedBlockEnd = "<!-- REACT_AMNESIA_AGENT_PACK:END -->";
const generatedNotice = [
    "This file is generated by `scripts/generate-agent-instructions.mjs`.",
    "Edit `website/docs/ai/*` and run `npm run docs:ai` instead of editing this file directly.",
].join(" ");

function normalizeNewlines(text) {
    return text.replace(/\r\n/g, "\n");
}

function stripFrontmatter(markdown) {
    return markdown.replace(/^---\n[\s\S]*?\n---\n*/, "").trim();
}

function readCanonicalDoc(fileName) {
    const filePath = path.join(docsDir, fileName);
    if (!existsSync(filePath)) {
        throw new Error(`Missing canonical AI doc: ${filePath}`);
    }
    return stripFrontmatter(normalizeNewlines(readFileSync(filePath, "utf8")));
}

function extractSection(markdown, heading) {
    const escapedHeading = heading.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
    const pattern = new RegExp(`^## ${escapedHeading}\\n\\n([\\s\\S]*?)(?=^## |(?![\\s\\S]))`, "m");
    const match = markdown.match(pattern);
    if (!match) {
        throw new Error(`Could not find section "## ${heading}" in canonical AI docs.`);
    }
    return match[1].trim();
}

function parseBullets(section) {
    return section
        .split("\n")
        .map((line) => line.trim())
        .filter((line) => line.startsWith("- "))
        .map((line) => line.slice(2));
}

function parseOrderedList(section) {
    return section
        .split("\n")
        .map((line) => line.trim())
        .filter((line) => /^\d+\.\s+/.test(line))
        .map((line) => line.replace(/^\d+\.\s+/, ""));
}

function parseRecipeTitles(markdown) {
    return Array.from(markdown.matchAll(/^## \d+\.\s+(.+)$/gm), (match) => match[1]);
}

async function formatMarkdown(body) {
    return await format(body.trimEnd() + "\n", {
        ...prettierConfig,
        parser: "markdown",
    });
}

function ensureParentDir(filePath) {
    mkdirSync(path.dirname(filePath), { recursive: true });
}

function readTextFileIfExists(filePath) {
    try {
        return normalizeNewlines(readFileSync(filePath, "utf8"));
    } catch (error) {
        if (error?.code === "ENOENT") {
            return null;
        }
        throw error;
    }
}

function writeTextFile(filePath, content) {
    const current = readTextFileIfExists(filePath);
    if (checkMode) {
        if (current !== content) {
            if (current === null) {
                throw new Error(`Generated agent instruction file is missing: ${filePath}. Run npm run docs:ai.`);
            }
            throw new Error(`Generated agent instruction file is out of date: ${filePath}. Run npm run docs:ai.`);
        }
        return;
    }
    ensureParentDir(filePath);
    writeFileSync(filePath, content, "utf8");
}

function mergeGeneratedBlock(existingText, generatedSection) {
    const normalizedExisting = normalizeNewlines(existingText).trimEnd();
    const wrappedSection = `${generatedBlockStart}\n${generatedSection.trimEnd()}\n\n${generatedBlockEnd}`;
    const blockPattern = new RegExp(
        `${generatedBlockStart.replace(/[.*+?^${}()|[\]\\]/g, "\\$&")}[\\s\\S]*?${generatedBlockEnd.replace(/[.*+?^${}()|[\]\\]/g, "\\$&")}`,
        "m",
    );

    if (blockPattern.test(normalizedExisting)) {
        return normalizedExisting.replace(blockPattern, wrappedSection) + "\n";
    }

    return `${normalizedExisting}\n\n${wrappedSection}\n`;
}

const overviewDoc = readCanonicalDoc("index.md");
const assistantSetupDoc = readCanonicalDoc("assistant-setup.md");
const recipesDoc = readCanonicalDoc("recipes.md");

const quickRules = parseBullets(extractSection(overviewDoc, "Quick Rules"));
const decisionChecklist = parseOrderedList(extractSection(overviewDoc, "Decision Checklist"));
const recipeTitles = parseRecipeTitles(recipesDoc);
const generatedInstructionPackPaths = parseBullets(
    extractSection(assistantSetupDoc, "Generated Instruction Packs"),
).map((filePath) => filePath.replace(/^`/, "").replace(/`$/, ""));

const canonicalDocs = [
    { label: "AI Overview", url: `${siteBaseUrl}/docs/ai` },
    { label: "Invariants", url: `${siteBaseUrl}/docs/ai/invariants` },
    { label: "Decision Matrix", url: `${siteBaseUrl}/docs/ai/decision-matrix` },
    { label: "Recipes", url: `${siteBaseUrl}/docs/ai/recipes` },
    { label: "Anti-Patterns", url: `${siteBaseUrl}/docs/ai/anti-patterns` },
    { label: "AI Assistant Setup", url: `${siteBaseUrl}/docs/ai/assistant-setup` },
];

const quickRulesMarkdown = quickRules.map((rule) => `- ${rule}`).join("\n");
const checklistMarkdown = decisionChecklist.map((item, index) => `${index + 1}. ${item}`).join("\n");
const recipeListMarkdown = recipeTitles.map((title) => `- ${title}`).join("\n");
const canonicalDocsMarkdown = canonicalDocs.map((doc) => `- [${doc.label}](${doc.url})`).join("\n");

function buildSharedInstructionBody(headingLevel = "##") {
    return `${headingLevel} Quick Rules

${quickRulesMarkdown}

${headingLevel} Decision Checklist

${checklistMarkdown}

${headingLevel} Canonical Recipes

${recipeListMarkdown}

${headingLevel} Canonical Docs

${canonicalDocsMarkdown}

${headingLevel} Maintenance

- Treat \`website/docs/ai/*\` as the canonical source.
- Run \`npm run docs:ai\` after changing canonical AI docs.
- Run \`npm run ai:check\` to verify generated instruction packs are in sync.
`;
}

